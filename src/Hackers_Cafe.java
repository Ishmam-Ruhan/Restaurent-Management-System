    private void BtneqlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtneqlActionPerformed
        num2=Double.parseDouble(display.getText());
        //caltempdisplay.setText(display.getText()+""+" "+"=");
        if(plus==true){
            result=num1+num2;
            String a=String.valueOf(num1);
            String b=String.valueOf(num2);
            caltempdisplay.setText(a+" "+"+"+" "+b+" "+"=");
            plus=false;
        }
        else if(minus==true){
            result=num1-num2;
            String a=String.valueOf(num1);
            String b=String.valueOf(num2);
            caltempdisplay.setText(a+" "+"-"+" "+b+" "+"=");
            minus=false;
        }
        else if(gun==true){
            result=num1*num2;
            String a=String.valueOf(num1);
            String b=String.valueOf(num2);
            caltempdisplay.setText(a+" "+"X"+" "+b+" "+"=");
            gun=false;
        }

        else if(vag==true){
            result=num1/num2;
            String a=String.valueOf(num1);
            String b=String.valueOf(num2);
            caltempdisplay.setText(a+" "+"/"+" "+b+" "+"=");
            vag=false;
        }
        else if(power==true){
            result=Math.pow(num1, num2);
            String a=String.valueOf(num1);
            String b=String.valueOf(num2);
            caltempdisplay.setText(a+" "+"^"+" "+b+" "+"=");
            power=false;
        }
        String aw=String.format("%.2f",result);
        display.setText(aw);
    }//GEN-LAST:event_BtneqlActionPerformed

